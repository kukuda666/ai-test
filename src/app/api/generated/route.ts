/*
* pages/api/personnel/report.ts
* This API route handles the reporting action for selected personnel IDs.
* It simulates updating the status of personnel records.
*/

import { NextApiRequest, NextApiResponse } from 'next';

// NOTE: In a real application, you would typically have a shared data source
// (e.g., a database service) that both `list.ts` and `report.ts` interact with.
// For this example, we'll re-declare a simplified mock data structure
// and simulate updates, but these updates won't persist across requests
// or be reflected in the `list.ts` mock data unless a more sophisticated
// in-memory store or actual database is used.

interface Personnel {
  id: string;
  status: 'pending' | 'reported' | 'failed';
}

// A highly simplified in-memory 'database' for demonstration.
// This will reset on server restart in development.
// In production, use a persistent database.
const personnelStore = new Map<string, Personnel>();

// Initialize with some mock data (only 'id' and 'status' relevant for this API)
// This should ideally be consistent with the data generated by list.ts
// For a proper solution, this mock data should be loaded from a shared module.
for (let i = 1; i <= 100; i++) {
  const id = `P${String(i).padStart(3, '0')}`;
  personnelStore.set(id, { id, status: Math.random() < 0.7 ? 'pending' : (Math.random() < 0.5 ? 'reported' : 'failed') });
}

export default function handler(req: NextApiRequest, res: NextApiResponse) {
  if (req.method === 'POST') {
    const { ids } = req.body; // `ids` is expected to be an array of personnel IDs

    if (!Array.isArray(ids) || ids.length === 0) {
      return res.status(400).json({ message: '请提供有效的ID列表进行报送。' });
    }

    // Simulate a successful reporting process with a slight delay
    setTimeout(() => {
      const reportedIds: string[] = [];
      const failedIds: string[] = [];

      ids.forEach((id: string) => {
        const personnel = personnelStore.get(id);
        if (personnel) {
          // In a real scenario, this would involve more complex logic,
          // e.g., calling an external service, checking business rules.
          // For mock, we simply update the status to 'reported'.
          personnel.status = 'reported'; // Update status in our mock store
          reportedIds.push(id);
        } else {
          failedIds.push(id);
        }
      });

      console.log(`Successfully reported IDs: ${reportedIds.join(', ')}`);
      if (failedIds.length > 0) {
        console.warn(`Failed to find/report IDs: ${failedIds.join(', ')}`);
      }

      // Send back a success response
      res.status(200).json({
        message: `成功报送 ${reportedIds.length} 位人员。${failedIds.length > 0 ? `有 ${failedIds.length} 位人员报送失败或未找到。` : ''}`,
        reportedCount: reportedIds.length,
        failedCount: failedIds.length,
        reportedIds: reportedIds,
        failedIds: failedIds,
      });
    }, 800); // Simulate network delay and processing time

  } else {
    // Handle unsupported HTTP methods
    res.setHeader('Allow', ['POST']);
    res.status(405).end(`Method ${req.method} Not Allowed`);
  }
}
